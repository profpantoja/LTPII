public class MyProgram {
    
    public static void main(String[] args) {
        
        int i;

        System.out.println("Ordem crescente:");
        for (i=0; i<=10; i++) {
            System.out.print(i + " ");
        }

        System.out.println();
        
        System.out.println("Ordem decrescente:");
        for (i=10; i>=0; i--) {
            System.out.print(i + " ");
        }
    }
}
